    def get_persona(self, name: str) -> Optional[AgentPersona]:
        return self._personas.get(name)

    def list_personas(self) -> List[str]:
        return list(self._personas.keys())

    def get_system_prompt(self, name: str) -> Optional[str]:
        p = self._personas.get(name)
        return p.system_prompt if p else None

    def register_persona(self, persona: AgentPersona):
        self._personas[persona.name] = persona

